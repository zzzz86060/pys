a = [-1]*5  
b = [-1]*5  
top1 = top2 = -1  
i = 0  
while i < 5:  
    ch = input("请输入操作: ")  # 修正了引号  
    if ch == "T":  # 修正了引号  
        top1 += 1  
        a[top1] = i
    else:  # 假设其他任何输入都代表要将a栈中的元素移到b栈中  
        if top2 == -1:  
            while top1 != -1:  
                top2 += 1  
                b[top2] = a[top1]  
                a[top1] = -1  
                top1 -= 1  
            b[top2] = -1  # 这一行应该移出while循环，否则会重复设置b的栈顶元素  
            top2 -= 1  # 修正：移动结束后应把b的栈顶指针也减一，如果需要的话  
        # 这里的逻辑可能需要根据你的具体需求进一步修改  
    i += 1  
